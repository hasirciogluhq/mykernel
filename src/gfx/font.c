static const uint8_t *glyph(char ch)
{
    if (ch < 32 || ch > 126)
        ch = '?';
    return font8x8[(unsigned char)ch - 32];
}

void gx_draw_char(gx_surface *s, int32_t x, int32_t y, char ch, gx_color c)
{
    const uint8_t *g = glyph(ch);
    for (int row = 0; row < 8; row++) {
        uint8_t bits = g[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1u << col))
                gx_surface_set(s, x + col, y + row, c);
        }
    }
}

void gx_draw_text(gx_surface *s, int32_t x, int32_t y, const char *text, gx_color c)
{
    if (!text)
        return;
    int32_t cx = x;
    while (*text) {
        if (*text == '\n') {
            cx = x;
            y += GX_FONT_H + 2;
            text++;
            continue;
        }
        gx_draw_char(s, cx, y, *text++, c);
        cx += GX_FONT_W;
    }
}

int gx_text_width(const char *text)
{
    int w = 0;
    int line = 0;
    if (!text)
        return 0;
    while (*text) {
        if (*text == '\n') {
            if (line > w)
                w = line;
            line = 0;
        } else {
            line += GX_FONT_W;
        }
        text++;
    }
    return line > w ? line : w;
}

int gx_text_height(void)
{
    return GX_FONT_H;
}
